              <p class="paragragh1">
              สตีเวน พอล จอบส์ (อังกฤษ: Steven Paul Jobs) หรือที่รู้จักในชื่อ สตีฟ จอบส์ (อังกฤษ: Steve Jobs; 24 กุมภาพันธ์ ค.ศ. 1955 - 5 ตุลาคม ค.ศ. 2011) เป็นผู้นำธุรกิจและนักประดิษฐ์ชาวอเมริกัน ผู้ร่วมก่อตั้ง ประธาน อดีตประธานบริหารของแอปเปิลคอมพิวเตอร์ และยังเคยเป็นประธานบริหาร พิกซาร์แอนิเมชันสตูดิโอส์ และเป็นคณะกรรมการบริหารบริษัทเดอะวอลต์ดิสนีย์ใน ค.ศ. 2006 หลังดิสนีย์ซื้อกิจการพิกซาร์
            </p>
               
            <p class="paragragh2">
                เขาร่วมก่อตั้งแอปเปิลคอมพิวเตอร์กับสตีฟ วอซเนียก ใน ค.ศ. 1976 เป็นผู้มีส่วนช่วยทำให้แนวความคิดเรื่องคอมพิวเตอร์ส่วนบุคคลเป็นที่นิยมขึ้นมา ด้วยเครื่อง Apple II ต่อมา เขาเป็นผู้แรกที่มองเห็นศักยภาพทางการค้าของส่วนประสานงานผู้ใช้แบบกราฟิกส์และเม้าส์ ที่ถูกพัฒนาขึ้นในศูนย์วิจัยซีร็อกซ์พาร์ค ของบริษัทซีร็อกซ์ และได้มีการผนวกเทคโนโลยีเหล่านี้เข้าไว้ในเครื่องแมคอินทอช หลังพ่ายแพ้ในการแย่งชิงอำนาจกับคณะกรรมการบริหารใน ค.ศ. 1984  จอบส์ลาออกจากแอปเปิลและก่อตั้งเน็กซ์ บริษัทพัฒนาแพลตฟอร์มคอมพิวเตอร์ โดยเฉพาะในการศึกษาขั้นอุดมศึกษาและตลาดธุรกิจ การซื้อกิจการเน็กซ์ของแอปเปิลใน ค.ศ. 1996 ทำให้จอบส์กลับเข้าทำงานในบริษัทแอปเปิลที่เขาร่วมก่อตั้งขึ้นนั้น และเขารับหน้าที่ CEO ตั้งแต่ ค.ศ. 1997 ถึง 2011 จอบส์ยังเป็นประธานบริหาร และผู้บริหารระดับสูงของพิกซาร์แอนิเมชันสตูดิโอส์ ผู้นำด้านการผลิตภาพยนตร์แอนิเมชันด้วยคอมพิวเตอร์กราฟิกส์ ทั้งเป็นผู้ถือหุ้นรายใหญ่ที่ 50.1% กระทั่งบริษัทวอลต์ดิสนีย์ซื้อกิจการไปใน ค.ศ. 2006 จอบส์เป็นผู้ถือหุ้นมากที่สุดของดิสนีย์ที่ 7% และเป็นสมาชิกคณะกรรมการบริหารของดิสนีย์
